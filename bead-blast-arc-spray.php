            <h4>Texturing</h4>
<p>Using bead blast, we can produce a range of controlled surface finishes with aluminum oxide or glass bead abrasive medians. Depending on what your adhesion and cosmetic specifications call for, we can create anything from a satin to a highly texturized surface. </p>
<br />
<h4>Protective Coatings</h4>
<p>We can add a Twin Wire Arc Spray (TWAS) thermal coating to metal and other substrates to extend the life of the part. This protective thermal bond layer reduces part wear and particle generation to increase the time between shield maintenance cycles. Our most common match layer surface coatings are Aluminum, Stainless Steel and Copper.</p>
<br />
<h4>Passivation (A 967- 05)</h4>
Stainless steel surfaces can be made more corrosive resistant by chemically dissolving the free ion iron particles and other oxides. This treatment creates a protective non-reactive film surface for electroplating or painting.
<br>
<br />
<h4>Minor Repairs</h4>
We can facilitate minor part repairs like small welds, broken screw removals and new screw threads. If the shield requires flattening to bring it back to conformity, we can arrange to have it annealed for you.



